public class Calculomaca {

   protected int macas;

   protected double total;
    protected double total2;

    public void setMacas(int macas) {
        this.macas = macas;
    }

    public double getTotal() {
        return total = (macas * 1.30);
    }

    public double getTotal2() {
        return total2 = (macas * 1.00);
    }
}




